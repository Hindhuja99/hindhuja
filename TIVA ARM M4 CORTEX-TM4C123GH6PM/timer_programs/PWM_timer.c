// PWM TIMER WITH SERVO MOTOR

#include <stdint.h>
#include "tm4c123gh6pm.h"
int main()
{
	SYSCTL_RCC_R |= 0x02400540;
	SYSCTL_RCGCGPIO_R = SYSCTL_RCGCGPIO_R5;
	GPIO_PORTF_DIR_R |= 0x00000002;
	GPIO_PORTF_DEN_R |= 0x00000002;
	GPIO_PORTF_AFSEL_R |= 0X00000002;
	GPIO_PORTF_PCTL_R |= GPIO_PCTL_PF1_T0CCP1;
	SYSCTL_RCGCTIMER_R |= SYSCTL_RCGCTIMER_R0;
	TIMER0_CTL_R &= ~TIMER_CTL_TBEN;
	TIMER0_CFG_R |= 0X00000004;
	TIMER0_TBMR_R |= TIMER_TBMR_TBAMS | TIMER_TBMR_TBMR_PERIOD;
	TIMER0_TBILR_R = 20000;
	TIMER0_CTL_R |= TIMER_CTL_TBEN;
	int i;
	while (1)
	{
		for (i = 17200;i < 19500;i++)
		{
			TIMER0_TBMATCHR_R = i;
			__delay_cycles(700);
		}


	}

}

